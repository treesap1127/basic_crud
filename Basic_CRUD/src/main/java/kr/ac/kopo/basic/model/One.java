package kr.ac.kopo.basic.model;

import java.util.Date;

public class One {
	private int one;
	private Date date;
	private String name;
	public int getOne() {
		return one;
	}
	public void setOne(int one) {
		this.one = one;
	}
	public Date getDate() {
		return date;
	}
	public void setDate(Date date) {
		this.date = date;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
